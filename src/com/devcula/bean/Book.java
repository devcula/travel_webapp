package com.devcula.bean;

public class Book {
  private String bookingObject;
  private String passengerArray;
  private String bookingType;
  private String userEmailId;
  
  
public String getUserEmailId() {
	return userEmailId;
}
public void setUserEmailId(String userEmailId) {
	this.userEmailId = userEmailId;
}
public String getBookingObject() {
	return bookingObject;
}
public void setBookingObject(String bookingObject) {
	this.bookingObject = bookingObject;
}
public String getPassengerArray() {
	return passengerArray;
}
public void setPassengerArray(String passengerArray) {
	this.passengerArray = passengerArray;
}
public String getBookingType() {
	return bookingType;
}
public void setBookingType(String bookingType) {
	this.bookingType = bookingType;
}

}
